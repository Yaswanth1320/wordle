/**
 * Get random word from specified difficulty level
 * @param {string} difficulty - 'easy' (4 letters), 'medium' (5 letters), 'hard' (8 letters)
 * @returns {string} - Random word from the specified difficulty
 */
export const getRandomWord = (difficulty = "medium") => {
  let wordList;

  switch (difficulty.toLowerCase()) {
    case "easy":
      wordList = WORDS_4;
      break;
    case "medium":
      wordList = WORDS_5;
      break;
    case "hard":
      wordList = WORDS_8;
      break;
    default:
      wordList = WORDS_5; // Default to medium
  }

  const randomIndex = Math.floor(Math.random() * wordList.length);
  return wordList[randomIndex];
};

/**
 * Get word length for specified difficulty
 * @param {string} difficulty - 'easy', 'medium', 'hard'
 * @returns {number} - Word length for the difficulty
 */
export const getWordLength = (difficulty) => {
  switch (difficulty.toLowerCase()) {
    case "easy":
      return 4;
    case "medium":
      return 5;
    case "hard":
      return 8;
    default:
      return 5;
  }
};
